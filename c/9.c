#include <stdio.h>
int main() {
    for (int i = 65; i <= 90; i++)
    {
        printf("%c ", i);
    }
    printf("\n");
    for (int i = 90; i >= 65; i--)
    {
        printf("%c ", i);
    }
    printf("\n");
    for (int i = 97; i <= 122; i++)
    {
        printf("%c ", i);
    }
    printf("\n");
    for (int i = 122; i >= 97; i--)
    {
        printf("%c ", i);
    }
    printf("\n");
    
}